package week1.day2;

public class Calculator {
	public int add(int a, int b, int c) {
		return a+b+c;
	}
	public int substract(int a1, int a2) {
		return a1-a2;
	}
	public double multiplay(double b1, double b2) {
		return b1*b2;
	}
	public float divide(float d1, float d2) {
		return d1/d2;
	}
}
